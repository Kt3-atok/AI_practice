from lmstudio_base import generate_response

# 相手の発言から信念候補を抽出する関数
# 引数 user_input: ユーザの発言
# 戻り値: 信念候補の文字列
def extract_belief(user_input):
    # システム指示（簡潔化ルール）
    system_inst = (
        "これは文章から信念を抽出するタスクです．"
        "与えられた相手(ユーザ)の発言から，それを受けた人物が考えること(信念)を抽出してください．"
        "事実ではなく，その発言を受けた人物が考えることを抽出してください．"

        "#ルール"
        "- 信念は「AはB」の命題の形で出力する"
        "- 意味と方向（肯定/否定・好き/嫌い）をそのまま正確に保つ。ぼかさない"
        "- 「〜が変化した」のような曖昧な要約は禁止。変化したなら「変化後の内容」を具体的に書く"
        "- 特定のイベントに対する信念は記述せず，広く適応できるもののみ記載する"
        "- 挨拶・相槌・一時的な雑談は抽出しない"
        "- 重要な事実がなければ、「<<該当なし>>」を返す"
    )

    few_shot = (
        "例①:\nユーザ: この前の旅行すっごく良かったんだよね！→　旅行はいいものだ\n悪い例: ユーザは旅行に行った（信念ではなく事実）\n\n"
        "例②:\nユーザ: 勉強さぼりすぎて補習になっちゃったよ...→　勉強は大切だ, ユーザは勉強すべきだ など\n悪い例: ユーザは勉強をしなかった, ユーザは補習になった（信念ではなく事実）\n\n"
        "例③:\nユーザ: 研究しんどいよー！来週までにプログラム完成させなくちゃいけなくてさぁ...→　研究は大変だ\n悪い例: 来週までにプログラムは完成させるべきだ（特定のイベントに対する認識）\n\n"
    )

    prompt = (
        f"{system_inst}\n\n{few_shot}\n\n質問: {user_input}\n回答:"
    )

    return generate_response(prompt)




if __name__ == "__main__":
    user_input = input("ユーザの発言を入力してください: ").strip()
    if not user_input:
        print("ユーザの発言を入力してください")
        exit()

    print(extract_belief(user_input))